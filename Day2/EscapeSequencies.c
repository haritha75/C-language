#include <stdio.h>

int main() {
    printf("Hello, World!\n");
    printf("This is a tab\tcharacter.\n");
    printf("A backslash: \\ and a double quote: \"\n");
    printf("This is a\b backspace.\n");
    printf("This is a carriage return.\r");
    printf("This text will overwrite the previous line.\n");
    printf("An audible alert: \a\n");
    printf("Vertical\vtab.\n");
    
    return 0;
}