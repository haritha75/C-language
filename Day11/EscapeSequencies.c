#include <stdio.h>

int main() {
    printf("Hello, World!\n");
    printf("This is a tab\tcharacter.\n"); // tab takes 8 spaces
    printf("A backslash: \\ and a double quote: \"\n");
    printf("This is a\b backspace.\n");
    printf("This is a carriage \r return.\n");
    printf("This text will overwrite the previous line.\n");
    printf("An audible alert: \a\n");
    printf("Vertical\vtab.\n");
    printf("hello\f world\frock the show"); //it forms new line where we can stop then on that place to new line line start
    
    return 0;
}